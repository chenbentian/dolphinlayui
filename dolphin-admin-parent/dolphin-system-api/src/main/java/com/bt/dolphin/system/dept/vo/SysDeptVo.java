package com.bt.dolphin.system.dept.vo;
/**
 * 系统部门表
 * @author cbt
 *
 */
public class SysDeptVo{
	
    private String deptNo;
    private String deptName;
    private String orgNo;
    private String typeCode;
    private String pDeptNo;
    private String dispSn;
	public String getDeptNo() {
		return deptNo;
	}
	public void setDeptNo(String deptNo) {
		this.deptNo = deptNo;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public String getTypeCode() {
		return typeCode;
	}
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	public String getpDeptNo() {
		return pDeptNo;
	}
	public void setpDeptNo(String pDeptNo) {
		this.pDeptNo = pDeptNo;
	}
	public String getDispSn() {
		return dispSn;
	}
	public void setDispSn(String dispSn) {
		this.dispSn = dispSn;
	}
    
    
}
